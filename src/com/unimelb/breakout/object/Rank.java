package com.unimelb.breakout.object;

/**
 * JSON Object
 * @author Siyuan Zhang
 *
 */
public class Rank {

	private String name;
	private int score;
	private int rank;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
}
